     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();
        
        $category = Category::select('url')->where('id',$data['category_id'])->first();
        $data['url'] = '/'.$category->url.'/'.str_replace(' ','-',$data['title']);
        Product::create($data);
        return \Redirect::to('product')->with('message', 'C saved successfully.');
    }

